void draw_character_on_window_buffer_at(UINT_32* character, WINDOW* wnd, UINT_32 relative_x_in_window, UINT_32 relative_y_in_window)
{
	UINT_32* trg = (UINT_32*)(wnd->buffer + ((relative_y_in_window * wnd->rect.width + relative_x_in_window) << 2));
	UINT_32* src = character;
	
	UINT_8 h = 12;
	UINT_8 w = 9;
	while(h--)
	{
		while(w--)
			*trg++ = *src++;
		trg = PHYSICAL_ADDRESS(trg) + (wnd->rect.width << 2) - 36;
		w = 9;
	}
}

